class Unet_att(nn.Module):
    """
    PyTorch implementation of a U-Net model.
    O. Ronneberger, P. Fischer, and Thomas Brox. U-net: Convolutional networks
    for biomedical image segmentation. In International Conference on Medical
    image computing and computer-assisted intervention, pages 234–241.
    Springer, 2015.
    """

    def __init__(
        self,
        in_chans: int,
        out_chans: int,
        chans: int = 32,
        num_pool_layers: int = 4,
        drop_prob: float = 0.0,
    ):
        """
        Args:
            in_chans: Number of channels in the input to the U-Net model.
            out_chans: Number of channels in the output to the U-Net model.
            chans: Number of output channels of the first convolution layer.
            num_pool_layers: Number of down-sampling and up-sampling layers.
            drop_prob: Dropout probability.
        """
        super().__init__()

        self.in_chans = in_chans
        self.out_chans = out_chans
        self.chans = chans
        self.num_pool_layers = num_pool_layers
        self.drop_prob = drop_prob

        self.down_sample_layers = nn.ModuleList([ConvBlock(in_chans, chans, drop_prob)])
        ch = chans
        for _ in range(num_pool_layers - 1):
            self.down_sample_layers.append(ConvBlock(ch, ch * 2, drop_prob))
            ch *= 2
        self.conv = ConvBlock(ch, ch * 2, drop_prob)
        self.gating = UnetGridGatingSignal(ch * 2, ch, kernel_size=(1, 1))
        self.up_conv = nn.ModuleList()
        self.up_transpose_conv = nn.ModuleList()
        self.attention_layers = nn.ModuleList()
        
        gating_ch = ch
        for _ in range(num_pool_layers - 1):
            self.up_transpose_conv.append(TransposeConvBlock(ch * 2, ch))
            self.up_conv.append(ConvBlock(ch * 2, ch, drop_prob))
                                        
            self.attention_layers.append(GridAttentionBlock2D(in_channels = ch, gating_channels = gating_ch, inter_channels = ch, sub_sample_factor = (2, 2), mode = 'concatenation')) # sub_sample_factor = (2, 2, 2)
            
            ch //= 2
        self.up_transpose_conv.append(TransposeConvBlock(ch * 2, ch))
        self.up_conv.append(
            nn.Sequential(
                ConvBlock(ch * 2, ch, drop_prob),
                nn.Conv2d(ch, self.out_chans, kernel_size=1, stride=1),
            )
        )

    def forward(self, image: torch.Tensor) -> torch.Tensor:
        """
        Args:
            image: Input 4D tensor of shape `(N, in_chans, H, W)`.
        Returns:
            Output tensor of shape `(N, out_chans, H, W)`.
        """
        stack = []
        output = image
#        print(output.shape)
        # apply down-sampling layers
#        i = 1
        for layer in self.down_sample_layers:
            output = layer(output)
            stack.append(output)
#            print(f"{output.shape} avg통과전 {i} layer")

            output = F.avg_pool2d(output, kernel_size=2, stride=2, padding=0)
#            print(f"{output.shape} {i} layer")
#            i = i + 1

        
        output = self.conv(output)
        gating = self.gating(output)
#        i = i - 1
        
        # apply up-sampling layers
        for transpose_conv, conv, att_blk in zip(self.up_transpose_conv, self.up_conv, self.attention_layers):
#            print(self.attention_layers)
#            print(att_blk)
#            print(f"{output.shape} 나오기 {i}-1")
            downsample_layer = stack.pop()
            g_conv, att = att_blk(downsample_layer, gating)
#            print("dddddddddddddddddddddddddddddddddddddddd")
            output = transpose_conv(output)
#            print(f"{output.shape} 나오기 {i}-2")
#             reflect pad on the right/botton if needed to handle odd input dimensions
            padding = [0, 0, 0, 0]
            if output.shape[-1] != downsample_layer.shape[-1]:
                padding[1] = 1  # padding right
                
            if output.shape[-2] != downsample_layer.shape[-2]:
                padding[3] = 1  # padding bottom
            
            if torch.sum(torch.tensor(padding)) != 0:
                output = F.pad(output, padding, "reflect")
            
            output = torch.cat([g_conv, output], dim=1)
#            print(f"{output.shape} 나오기 {i}-3")
            output = conv(output)
#            print(f"{output.shape} 나오기 {i}-4")
#            i = i -1
        
        last_transpose = self.up_transpose_conv[-1]
        last_conv = self.up_conv[-1]
        downsample_layer = stack.pop()
        output = last_transpose(output)
        
        padding = [0, 0, 0, 0]
        if output.shape[-1] != downsample_layer.shape[-1]:
            padding[1] = 1  # padding right

        if output.shape[-2] != downsample_layer.shape[-2]:
            padding[3] = 1  # padding bottom

        if torch.sum(torch.tensor(padding)) != 0:
            output = F.pad(output, padding, "reflect")

        output = torch.cat([downsample_layer, output], dim = 1)
        output = last_conv(output)
            
        return output


